const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// Creating user schema
const UserSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    password2: {
        type: String,
        required: true
    },
    avatar: {
        type: String
    },
    date: {
        type: Date,
        default: Date.now
    }
});

// Converting the above schema to a model
module.exports = user = mongoose.model('users', UserSchema);